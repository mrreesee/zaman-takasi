    public static readonly string[] SupportedCultures = { "en", "tr" };

    public static string Get(string culture, string key)
    {
        var lang = (culture ?? DefaultCulture).Split('-')[0].ToLowerInvariant();
        if (lang == "tr" && Tr.TryGetValue(key, out var tr)) return tr;
        if (En.TryGetValue(key, out var en)) return en;
        return key;
    }

